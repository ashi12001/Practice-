public class  _1And {
                                        //and or xor
       public static void main(String[] args) {

        System.out.println(3&4); //  011  100
        System.out.println(4&5);    //101
        System.out.println(5&6);    //110


        System.out.println("for or operator "+ (3|4));
        System.out.println("for or operator "+ (4|5));
        System.out.println("for or operator "+ (5|6));



        System.out.println("\nfor xor operator "+ (4^5));
        System.out.println("for xor operator "+ (5^5));
        System.out.println("for xor operator "+ (4^6));



        System.out.println("\nfor negation operator "+ (~5));  //complement of 5 + 1 is 2 s compliment 101  010


        System.out.println();
                                        
                                        
                                        
  }
}