public class _2LeftAnddRightShift {
                                        
       public static void main(String[] args) {


        System.out.println("\nleft shift is "+ (5<<2));
        System.out.println("left shift is "+ (5<<3));
        System.out.println("left shift is "+ (5<<4)); // 5 * 2^4



        System.out.println("\nright shift is "+ (6>>1));  // 6 /2
        System.out.println("right shift is "+ (6>>2));  // 6 /2^2
        System.out.println("right shift is "+ (24>>1));  // 24 /2
                                        
                                        
                                        
  }
}